cars = ['bmw','audi','toyota','subaru']
cars.sort(); #sorts the list permanently
print("Asending order")
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True) #sort the list in reverse order
print("Decending order");
print(cars)


cars = ['bmw','audi','toyota','subaru']
print("Here is the original list : ")
print(cars)

print("Here is the sorted list ")
print(sorted(cars))


print("Cars in reverse order")
cars.reverse() #reverses the array permanently
print(cars);
